package day14;

public class StringMethodCharAt {
    public static void main(String[] args) {
        String name="Aysegul";

        System.out.println( name.charAt(0));
        System.out.println( name.charAt(1));
        System.out.println( name.charAt(2));
        System.out.println( name.charAt(3));
        System.out.println( name.charAt(4));
        System.out.println( name.charAt(5));
        System.out.println( name.charAt(6));

        String name2= "Aysegul";
        char c1=name2.charAt(0);
        char c2=name2.charAt(1);
        char c3=name2.charAt(2);
        char c4=name2.charAt(3);
        char c5=name2.charAt(4);
        char c6=name2.charAt(5);
        char c7=name2.charAt(6);

        System.out.println(c1+ " " + c2 + " " + c3 + " " + c4
                + " " + c5 + " " + c6 + " " + c7 );
    }
}
